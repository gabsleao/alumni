                <h5>Conceito</h5>
                <p>Em uma <b>instituição inclusiva</b> o processo educativo deve ser entendido como um processo social,
                    onde todas as crianças portadoras de necessidades especiais e de distúrbios de aprendizagem têm o direito à escolarização
                    o mais próximo possível do normal. Ou seja, <b>uma modalidade de ensino para todos.</b></p>

                <p>A inclusão promove a diversidade.
                    A ideia da inclusão é mais do que somente garantir o acesso à entrada de alunos e alunas nas instituições de ensino.
                    O objetivo é eliminar obstáculos que limitam a aprendizagem e participação discente no processo educativo.</p>

                <h5>Aplicação</h5>
                <p>A maioria das instituições de ensino regulares no Brasil não estão preparadas para receberem e ensinarem aos alunos com deficiência, devido a 
                    problema de infraestrutura e formação profissional da equipe.
                    Hoje o modelo é que instituições inclusivas, que teoricamente têm o conhecimento da educação especial, se transformem em centros de 
                    recursos para apoiar o ensino inclusivo em todas as instituições de ensino que estão na sua região, com professores itinerantes e materiais pedagógicos.</p>
            
